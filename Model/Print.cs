using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Expire.Model
{
    class Print
    {
        private int id;
        private string name;
        private string batich;
        private string amount;
        private string store;
        private string username;

        public int Id
        {
            get { return id; }
            set { id = value; }
        }




        public string Name
        {
            get { return name; }
            set { name = value; }
        }

        public string Batich
        {
            get { return batich; }
            set { batich = value; }
        }

        public string Amount
        {
            get { return amount; }
            set { amount = value; }
        }

        public string Store
        {
            get { return store; }
            set { store = value; }
        }

        public string Username
        {
            get { return username; }
            set { username = value; }
        }



   


    }
}
